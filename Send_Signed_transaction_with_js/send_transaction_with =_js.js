const fromAddress = 'add your own wallet address';
const toAddress= '0x51963006732702Fc553eF9f00b13032DE4881db9';
const privateKey = 'Add your wallet private key ';

async function signed_transaction() {

    var value =web3.utils.toWei('0.1','ether');
    console.log(value);

    
    const functionSignature = contract.methods.transfer(toAddress, value).encodeABI();
    console.log(functionSignature);
    const nonce = await web3.eth.getTransactionCount(fromAddress);
    console.log("nonce: " ,nonce);
    const gasPrice = await web3.eth.getGasPrice();
    
    const gasLimit = 300000;
    const transaction = {
        from: fromAddress,
        to: contractAddress,
        nonce: nonce,
        gasPrice: gasPrice,
        gasLimit: gasLimit,
        data: functionSignature
    };
    console.log(transaction);
    const signedTransaction = await web3.eth.accounts.signTransaction(transaction, privateKey);
    console.log(signedTransaction);
    // broadcast the transaction
    const receipt = await web3.eth.sendSignedTransaction(signedTransaction.rawTransaction);

    // wait for confirmation
    const confirmation = await web3.eth.getTransactionReceipt(receipt.transactionHash);
    console.log(confirmation);
}
    signed_transaction();
